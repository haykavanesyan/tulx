/**
 * Adds two numbers.
 *
 * @param augend - The first number in an addition.
 * @param addend - The second number in an addition.
 * @returns Returns the sum.
 *
 * @example
 * ```ts
 * add(6, 4); // 10
 * ```
 */
export function add(augend: number, addend: number): number {
  return augend + addend;
}
